package com.proyecto.leds;

public class Data {
    private int Luz;
    private int Pulsador;
    private int Potenciometro;

    public int getLuz() {
        return Luz;
    }

    public void setLuz(int luz) {
        Luz = luz;
    }

    public int getPulsador() {
        return Pulsador;
    }

    public void setPulsador(int pulsador) {
        Pulsador = pulsador;
    }

    public int getPotenciometro() {
        return Potenciometro;
    }

    public void setPotenciometro(int potenciometro) {
        Potenciometro = potenciometro;
    }
}
